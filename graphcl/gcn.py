import torch
import torch.nn as nn
from torch_geometric.nn.inits import glorot
class GCNPrompt(nn.Module):
    def __init__(self, in_ft, out_ft, act, nb_nodes,bias=True, ):
        super(GCNPrompt, self).__init__()
        self.fc = nn.Linear(in_ft, out_ft, bias=False)
        self.act = nn.PReLU() if act == 'prelu' else act
        self.prompt = nn.Parameter(torch.FloatTensor(1, 1, in_ft), requires_grad=True)
        torch.nn.init.xavier_uniform_(self.prompt)

        if bias:
            self.bias = nn.Parameter(torch.FloatTensor(out_ft))
            self.bias.data.fill_(0.0)
        else:
            self.register_parameter('bias', None)

        for m in self.modules():
            self.weights_init(m)

    def weights_init(self, m):
        if isinstance(m, nn.Linear):
            torch.nn.init.xavier_uniform_(m.weight.data)
            if m.bias is not None:
                m.bias.data.fill_(0.0)

    # Shape of seq: (batch, nodes, features)
    def forward(self, seq, adj, sparse=True):
        seq = torch.add(seq, self.prompt)
        seq_fts = self.fc(seq)
        # seq_fts = torch.add(seq_fts, self.prompt)
        if sparse:
            out = torch.unsqueeze(torch.spmm(adj, torch.squeeze(seq_fts, 0)), 0)
        else:
            out = torch.bmm(adj, seq_fts)
        if self.bias is not None:
            out += self.bias

        return self.act(out)
